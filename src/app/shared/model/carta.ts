export class Carta {
    nome: string;
    hp: number;
    attack: string;
    weakness: string;
    image: string;

    constructor(nome: string, hp: number, attack: string, weakness: string, image: string) {
        this.nome = nome;
        this.hp = hp;
        this.attack = attack;
        this.weakness = weakness;
        this.image = image
    }




}